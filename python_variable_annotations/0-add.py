#!/usr/bin/env python3
'''
This module provides one function that perform a sum of two parameters.

Usage:
 - Import: __import__('0-add').add
     
Note: This module requires Python 3.7 or above.
'''


def add(a: float, b: float) -> float:
    '''
    This is a type-annotated function that adds two parameters.

    Parameters:
    - a (float): number
    - b (float): number

    Returns:
    - float: a + b
    '''
    return a + b
